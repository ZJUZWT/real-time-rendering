void SHFSample(SHCoef& res, const int SH_ORDER, Float x, Float z, Float y) {
	Float r = sqrt(x * x + y * y + z * z);
	x /= r;
	y /= r;
	z /= r;
	if (SH_ORDER == 0) SHFSampler_0(res, x, y, z);
	else 	if (SH_ORDER == 1) SHFSampler_1(res, x, y, z);
	else 	if (SH_ORDER == 2) SHFSampler_2(res, x, y, z);
	else 	if (SH_ORDER == 3) SHFSampler_3(res, x, y, z);
	else 	if (SH_ORDER == 4) SHFSampler_4(res, x, y, z);
	else 	if (SH_ORDER == 5) SHFSampler_5(res, x, y, z);
	else 	if (SH_ORDER == 6) SHFSampler_6(res, x, y, z);
	else 	if (SH_ORDER == 7) SHFSampler_7(res, x, y, z);
	else 	if (SH_ORDER == 8) SHFSampler_8(res, x, y, z);
	else 	if (SH_ORDER == 9) SHFSampler_9(res, x, y, z);
	else 	if (SH_ORDER == 10) SHFSampler_10(res, x, y, z);
	else 	if (SH_ORDER == 11) SHFSampler_11(res, x, y, z);
	else 	if (SH_ORDER == 12) SHFSampler_12(res, x, y, z);
	else 	if (SH_ORDER == 13) SHFSampler_13(res, x, y, z);
	else 	if (SH_ORDER == 14) SHFSampler_14(res, x, y, z);
	else 	if (SH_ORDER == 15) SHFSampler_15(res, x, y, z);
	else 	if (SH_ORDER == 16) SHFSampler_16(res, x, y, z);
	else 	if (SH_ORDER == 17) SHFSampler_17(res, x, y, z);
	else  if (SH_ORDER == 18) SHFSampler_18(res, x, y, z);
	else {
		SHFSampler_18(res, x, y, z);
		for (int l = 19; l <= SH_ORDER; l++)
			for (int m = -l; m <= l; m++)
				res[l * l + m + l] = EvalSHSlow(l, m, atan2(z, x), acos(y));
	}
}